const { config } = require("dotenv");
const fs = require("fs");

var readConfig = function () {
    let rt = "okunamadı";
    try {
        const data = fs.readFileSync('config.json', 'utf8');
        rt = JSON.parse(data);
    } 
    catch (err) {
        rt = err;
    }
    return rt;
}
exports.readConfig = readConfig;
